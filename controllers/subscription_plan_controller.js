const SubscriptionPlan = require("../models/subscription_plan.model");
const mongoose = require("mongoose");
const { baseStatus, userStatus } = require("../utils/enumerator");

exports.create_subscription_plan = (req, res, next) => {
  if (!req.body) {
    res.status(400).send({
      status: false,
      message: "body missing",
    });
  } else {
    try {
      SubscriptionPlan(req.body)
        .save()
        .then((result) => {
          if (result) {
            res
              .status(201)
              .send({ status: true, message: "success", data: result });
          } else {
            res.status(404).send({ status: false, message: "Not created" });
          }
        })
        .catch((error) => {
          res.send({
            status: false,
            message: error.toString() ?? "Error",
          });
        });
    } catch (error) {
      res.status(500).send({
        status: false,
        message: "failure",
        error: error ?? "Internal Server Error",
      });
    }
  }
};

exports.get_subscription_plans = async (req, res) => {
  const page = parseInt(req.query.page) || 1;
  const limit = parseInt(req.query.limit) || 10;
  const myCustomLabels = {
    totalDocs: "totalDocs",
    docs: "data",
    limit: "limit",
    page: "page",
    nextPage: "nextPage",
    prevPage: "prevPage",
    totalPages: "totalPages",
    pagingCounter: "slNo",
    meta: "paginator",
  };

  const options = {
    page: page,
    limit: limit,
    customLabels: myCustomLabels,
  };
  
  try {
    var myAggregate = SubscriptionPlan.aggregate([
      {
        $match: {
          status: baseStatus.active,
        },
      },
      {
        $sort: { createdAt: -1 },
      },
    ]);
    
    await SubscriptionPlan.aggregatePaginate(myAggregate, options)
      .then((result) => {
        if (result) {
          // Map through the result data and replace name with plan_name
          const transformedData = result.data.map(plan => {
            const { name, ...rest } = plan;
            return { plan_name: name, ...rest };
          });

          // Send the transformed data in the response
          res.status(200).send({
            status: true,
            message: "success",
            data: {
              ...result,
              data: transformedData, // Replace the original data with the transformed data
            },
          });
        }
      })
      .catch((error) => {
        res.send({
          status: false,
          message: error.toString() ?? "Error",
        });
      });
  } catch (error) {
    res.status(500).send({
      status: false,
      message: error.toString() ?? "Internal Server Error",
    });
  }
};

